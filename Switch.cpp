void workSwitch(){
	int a;
	setlocale(LC_ALL, "Rus");
	cout << "Введите любое число: ";
	cin >> a;
	switch (a) {
	case 1:
		cout << "Вы ввели 1";
		break;
	case 2:
		cout << "Вы ввели 2";
		break;
	case 3:
		cout << "Вы ввели 3";
		break;
	case 4:
		cout << "Вы ввели 4";
		break;
	default:
		cout << "Вы ввели число, большее 4";
		break;
	}
}